package com.addrecord.model;

public class TodayTotalVO {
	
	int totalMoney;
	int totalPoint;
	int totoalConsumption;
	int totalChange;
	
	public int getTotalMoney() {
		return totalMoney;
	}
	public void setTotalMoney(int totalMoney) {
		this.totalMoney = totalMoney;
	}
	public int getTotalPoint() {
		return totalPoint;
	}
	public void setTotalPoint(int totalPoint) {
		this.totalPoint = totalPoint;
	}
	public int getTotoalConsumption() {
		return totoalConsumption;
	}
	public void setTotoalConsumption(int totoalConsumption) {
		this.totoalConsumption = totoalConsumption;
	}
	public int getTotalChange() {
		return totalChange;
	}
	public void setTotalChange(int totalChange) {
		this.totalChange = totalChange;
	}

}
